#Sistema para Calcular el Cuadrado de un Número

nombre = input("¿Cúal es tu nombre? ")
print ("Hola " + nombre + ", vamos a calcular el cuadrado de un número ")
num_uno = int(input("Por favor introduce el primer valor: "))
exponente = 2
resultado = num_uno ** exponente

print (nombre + " el resultado del calculo del cuadrado del número es: " , resultado)
print ("Fin.")